        function init() {
            const deptId = getDeptIdFromURL();
            
            if (!deptId) {
                showNotFound();
                return;
            }

            const department = depts.find(d => d.id === deptId);
            if (department) {
                renderDepartment(department);
            } else {
                showNotFound();
            }
        }
